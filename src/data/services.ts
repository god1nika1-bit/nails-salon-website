// Flat list for search
export function getAllServices(): (ServiceItem & { category: string; subcategory: string })[] {
  const result: (ServiceItem & { category: string; subcategory: string })[] = [];
  for (const cat of SERVICES_DATA) {
    for (const sub of cat.subcategories) {
      for (const item of sub.items) {
        result.push({ ...item, category: cat.name, subcategory: sub.title });
      }
    }
  }
  return result;
}

// Total count
export function getTotalServicesCount(): number {
  return SERVICES_DATA.reduce(
    (total, cat) => total + cat.subcategories.reduce(
      (subTotal, sub) => subTotal + sub.items.length, 0
    ), 0
  );
}
